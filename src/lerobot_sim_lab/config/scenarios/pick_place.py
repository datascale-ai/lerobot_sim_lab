def get_scenario(scenario_id):
    """获取场景配置"""
    for scenario in SCENARIOS:
        if scenario['id'] == scenario_id:
            return scenario
    raise ValueError(f"场景 {scenario_id} 不存在，可用场景: {[s['id'] for s in SCENARIOS]}")


def get_num_scenarios():
    """获取已定义的场景数量"""
    return len(SCENARIOS)


def list_scenarios():
    """列出所有可用场景"""
    print(f"\n{'='*80}")
    print(f"场景库统计：共 {len(SCENARIOS)} 个场景")
    print(f"{'='*80}\n")
    for scenario in SCENARIOS:
        print(f"场景 {scenario['id']}: {scenario['name']}")
        print(f"  Cube位置: {scenario['cube_pos']}")
        print(f"  抓取配置: {scenario['pick_config']}")
        print()


